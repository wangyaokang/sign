package com.wyk.sign.web.api.param;

public class UploadFile {

    /** 文件序号 */
    private String no;

    /** 文件名 */
    private String name;

    /** 文件url */
    private String url;

    public String getNo() {
        return no;
    }

    public void setNo(String no) {
        this.no = no;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

}
